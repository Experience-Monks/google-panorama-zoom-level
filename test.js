var test = require('tape')
var level = require('./')

test('should report level', function (t) {
  t.equal(level(0), -1)
  t.equal(level(415), -1)
  t.equal(level(416), 0)
  t.equal(level(800), 0)
  t.equal(level(850), 1)
  t.equal(level(1024), 1)
  t.equal(level(1800), 2)
  t.equal(level(3327), 2)
  t.equal(level(3328), 3)
  t.equal(level(4096), 3)
  t.equal(level(6655), 3)
  t.equal(level(6656), 4)
  t.equal(level(13311), 4)
  t.equal(level(13312), 5)
  t.equal(level(20000), 5)
  t.throws(level.bind(null), 'handles error')
  t.end()
})
